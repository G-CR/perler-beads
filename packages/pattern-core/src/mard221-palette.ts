function parseHexToRgb(hex: string): RgbColor {
  return {
    r: Number.parseInt(hex.slice(1, 3), 16),
    g: Number.parseInt(hex.slice(3, 5), 16),
    b: Number.parseInt(hex.slice(5, 7), 16),
  }
}

function compactMardCode(code: string): string {
  const match = code.match(/^([A-Z]+)0*(\d+)$/)
  if (!match) {
    return code
  }

  const [, prefix, numericPart] = match
  return `${prefix}${Number.parseInt(numericPart, 10)}`
}

export const MARD221_PALETTE: Mard221PaletteEntry[] = MARD221_CSV.split('\n').map(line => {
  const [code, hex] = line.split(',')
  if (!code || !hex) {
    throw new Error(`Invalid MARD 221 palette entry: ${line}`)
  }

  return {
    code: compactMardCode(code),
    hex,
    rgb: parseHexToRgb(hex),
  }
})
